package Day1;

public class Second {

	public static void main(String[] args) {
		int i=5;
		int x=8;
		int z=i+x;
		System.out.println(z);
		
		String a="Kishan";
		String b="Mishra";
		String c = a +" " +b;
		System.out.println(c);
		
	// type conversion
		
		// automatic conversion
		int aa = 50;
		double d=aa;
		System.out.println(aa);
		System.out.println(d);

		// forcefully conversion
		double d1=40.55;
		int d2=(int) d1;
		System.out.println(d2);
	}

}
